export function getExcerpt(body: string, lines = 3): string {
  const trimmedLines = body
    .split(/\n/)
    .map((line) => line.trim())
    .filter(Boolean)
    .slice(0, lines);
  return trimmedLines.join(" ").replace(/\s+/g, " ").trim();
}

export function getStudentLifeSection(
  id: StudentLifeSectionId
): StudentLifeSection | undefined {
  return STUDENT_LIFE_SECTIONS.find((s) => s.id === id);
}
