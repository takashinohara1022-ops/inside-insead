/** 本文の冒頭を指定行数で切り取り、1行テキストとして返す（概要ページのプレビュー用） */
export function getExcerpt(body: string, lines = 3): string {
  const trimmedLines = body
    .split(/\n/)
    .map((line) => line.trim())
    .filter(Boolean)
    .slice(0, lines);
  return trimmedLines.join(" ").replace(/\s+/g, " ").trim();
}

/** id でセクションを取得 */
export function getAboutSection(id: AboutSectionId): AboutSection | undefined {
  return ABOUT_SECTIONS.find((s) => s.id === id);
}
